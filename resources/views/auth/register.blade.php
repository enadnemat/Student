    <script>
        $("#submitForm").validate({
            rules: {
                username: {
                    required: true,
                },
                email: {
                    required: true,
                    email: true,
                },
                password: {
                    required: true,
                    pattern: "^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$",
                },
                passwordConfirm: {
                    required: true,
                    minLength: 8,
                    equalTo: "#password",
                }
            },
            messages: {
                username: {
                    required: "Username is required"
                },
                email: {
                    required: "Email is required",
                    email: "Check your email type",
                },
                password: {
                    required: "Password is required",
                    minLength: "Password should be at least 8 char",
                },
                passwordConfirm: {
                    required: "Password is required",
                    minLength: "Password should be at least 8 char",
                    equalTo: "Confirm password should be the same as the password",
                }
            },
            submitHandler: function (form) {
                var formData = new FormData(Document.form['submitForm']);

                $.ajax({
                    url: "{{Route('register')}}",
                    type: 'POST',
                    data: formData,
                    processData: false,
                    contentType: false,
                    cache: false,
                    headers: {
                        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                    },
                    success: function (data) {
                        $('#submitForm').trigger("reset");
                    },
                    error: function (reject) {
                    },
                })
            }
        })
